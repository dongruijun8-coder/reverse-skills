def hook_gen_frida(target_classes: list[str] | None = None,
                   script_type: str = "crypto",
                   template: str | None = None,
                   package: str = "",
                   output_dir: str | None = None) -> dict:
    """Generate a Frida JavaScript hook script with version tracking.

    Args:
        target_classes: List of "com.example.ClassName.method" strings.
                        If None, uses defaults based on script_type or template.
        script_type: Preset — "crypto", "ssl", "app_layer", "gson", "full", or "template".
        template: Named template T1-T8 from kb/patterns/frida_hook_templates.md.
                  Overrides script_type when set.
        package: Target app package name (for version tracking directory).
        output_dir: Directory to save script. Auto-creates subdir if package provided.
    """
    if target_classes:
        targets = []
        for tc in target_classes:
            parts = tc.rsplit('.', 1)
            if len(parts) == 2:
                targets.append((parts[0], parts[1], []))
            else:
                targets.append((tc, "*", []))
    elif template and template.lower() in TEMPLATE_TARGETS:
        targets = TEMPLATE_TARGETS[template.lower()]
        template_name = template.lower()
        # T3 requires custom target_classes — warn if empty
        if template_name == "t3" and not targets:
            return {
                "status": "ERROR",
                "error": "Template T3 requires target_classes. "
                         "The HttpClientImp class name varies per app. "
                         "Provide target_classes=['com.example.http.HttpClientImp.createCall'] "
                         "after finding the implementation class.",
                "hint": "Search APK strings for 'createCall' or 'HttpClient' to find the class name.",
            }
    elif script_type == "ssl":
        targets = SSL_UNPIN_TARGETS
    elif script_type == "app_layer":
        targets = APP_LAYER_TARGETS
    elif script_type == "gson":
        targets = GSON_TARGETS
    elif script_type == "full":
        targets = CRYPTO_TARGETS + APP_LAYER_TARGETS + GSON_TARGETS
    else:
        targets = CRYPTO_TARGETS
        template_name = None

    # Resolve template_name for output metadata
    resolved_template = template_name if template_name else None

    # Build hook code
    hooks_code = []
    for full_class, method, params in targets:
        class_name = full_class.replace('.', '_')
        param_str = ', '.join(params) if params else ''
        log_str = ' + ", " + '.join(params) if params else '""'
        send_str = ', '.join(f'{p}.toString()' for p in params) if params else ''

        # Use createCall-specific template for app-layer methods that need param inspection
        if method == "newCall" and "OkHttpClient" in full_class:
            # Standard hook for newCall — just log the request
            hooks_code.append(CLASS_HOOK_TEMPLATE.format(
                class_name=class_name, full_class=full_class, method=method,
                params=param_str, log_params=log_str, send_params=send_str
            ))
        elif method == "getData" and "RequestBody" in full_class:
            # Body.getData — return bytes, log length
            hooks_code.append(f"""    var {class_name} = Java.use("{full_class}");
    {class_name}.{method}.implementation = function({param_str}) {{
        var result = this.{method}({param_str});
        console.log("[REVERSE] Body.getData() = " + result.length + " bytes");
        send({{"hook": "Body.getData", "bytes": result.length, "hex": bytesToHex(result)}});
        return result;
    }};

    function bytesToHex(bytes) {{
        var hex = [];
        for (var i = 0; i < Math.min(bytes.length, 128); i++) {{
            hex.push(("0" + (bytes[i] & 0xFF).toString(16)).slice(-2));
        }}
        return hex.join("");
    }}
""")
        else:
            hooks_code.append(CLASS_HOOK_TEMPLATE.format(
                class_name=class_name, full_class=full_class, method=method,
                params=param_str, log_params=log_str, send_params=send_str
            ))

    # Version tracking: auto-increment version in output dir
    version = 1
    prefix = resolved_template if resolved_template else script_type
    if output_dir and package:
        hook_dir = Path(output_dir) / "hooks"
        hook_dir.mkdir(parents=True, exist_ok=True)
        existing = sorted(hook_dir.glob(f"{prefix}_v*.js"))
        if existing:
            last = existing[-1].stem
            m = re.search(r'v(\d+)$', last)
            if m:
                version = int(m.group(1)) + 1
        output_path = str(hook_dir / f"{prefix}_v{version}.js")
    elif output_dir:
        output_path = str(Path(output_dir) / f"hook_{prefix}.js")
    else:
        output_path = None

    safety = TEMPLATE_SAFETY.get(resolved_template, "") if resolved_template else SAFETY_NOTES.get(script_type, "")
    strategy = f"template-{resolved_template}" if resolved_template else STRATEGY_NOTES.get(script_type, "custom")

    script = FRIDA_HOOK_TEMPLATE.format(
        version=f"v{version}",
        preset=resolved_template or script_type,
        package=package or "unknown",
        strategy=f"strategy: {strategy}",
        safety_notes=f"Safety: {safety}",
        hooks='\n'.join(hooks_code)
    )

    if output_path:
        Path(output_path).write_text(script, encoding='utf-8')
        result = {
            "status": "OK",
            "output_path": output_path,
            "version": version,
            "preset": resolved_template or script_type,
            "target_count": len(targets),
            "targets": [f"{c}.{m}" for c, m, _ in targets],
            "safety": safety,
        }
        if resolved_template:
            result["template"] = resolved_template
            result["template_library"] = "kb/patterns/frida_hook_templates.md"
            result["description"] = TEMPLATE_DESCRIPTIONS.get(resolved_template, "")
        return result

    result = {"status": "OK", "script": script, "version": version,
              "preset": resolved_template or script_type, "target_count": len(targets)}
    if resolved_template:
        result["template"] = resolved_template
        result["template_library"] = "kb/patterns/frida_hook_templates.md"
    return result


def hook_run(method: str, script_path: str, package: str,
             server_path: str | None = None, timeout: int = 60) -> dict:
    """Run a hook script using Frida or hluda.

    Args:
        method: "frida" (spawn), "hluda" (attach via hluda-server),
                "frida-attach" (attach via frida-server), "gadget", "lsposed"
        script_path: Path to the hook script
        package: Target app package name
        server_path: Path to hluda-server binary (for method="hluda")
        timeout: Max seconds to run before timing out
    """
    if not Path(script_path).exists():
        return {"status": "ERROR", "error": f"Script not found: {script_path}"}

    if method in ("frida",):
        # Spawn mode — app is restarted with frida injected
        try:
            result = subprocess.run(
                ["frida", "-U", "-l", script_path, "-f", package, "--no-pause"],
                capture_output=True, text=True, timeout=timeout
            )
            crashed = "SIGSEGV" in (result.stderr + result.stdout)
            detected = crashed or "process terminated" in result.stdout.lower()
            return {
                "status": "DETECTED" if detected else "OK",
                "mode": "spawn",
                "stdout": result.stdout[:3000],
                "stderr": result.stderr[:500],
                "crashed": crashed,
                "detected": detected,
            }
        except subprocess.TimeoutExpired:
            return {"status": "TIMEOUT", "stdout": "", "stderr": "Frida timed out", "mode": "spawn"}
        except FileNotFoundError:
            return {"status": "ERROR", "error": "frida not installed. pip install frida-tools"}

    elif method in ("hluda", "frida-attach"):
        # Attach mode — app already running, inject into process
        # hluda-server must be running on device (started in Phase 0.5)
        try:
            result = subprocess.run(
                ["frida", "-U", "-l", script_path, "-n", package, "--no-pause"],
                capture_output=True, text=True, timeout=timeout
            )
            crashed = "SIGSEGV" in (result.stderr + result.stdout)
            detected = crashed or "process terminated" in result.stdout.lower()

            # Check if attach failed (app not running, etc.)
            if "unable to find process" in result.stderr.lower():
                return {
                    "status": "ERROR",
                    "error": f"Process '{package}' not found. Is the app running? Start it first.",
                    "mode": "attach",
                }

            return {
                "status": "DETECTED" if detected else "OK",
                "mode": "attach",
                "server": "hluda" if method == "hluda" else "frida-server",
                "stdout": result.stdout[:3000],
                "stderr": result.stderr[:500],
                "crashed": crashed,
                "detected": detected,
            }
        except subprocess.TimeoutExpired:
            return {"status": "OK", "mode": "attach",
                    "note": "Hook running (timeout reached). Script may still be active.",
                    "stdout": "", "stderr": ""}
        except FileNotFoundError:
            return {"status": "ERROR", "error": "frida not installed. pip install frida-tools"}

    elif method == "gadget":
        return {
            "status": "TODO",
            "note": "Frida Gadget requires injecting libfrida-gadget.so into the APK. "
                    "Use apk_unpack → inject libfrida-gadget.so → repackage → install.",
        }

    elif method == "lsposed":
        return {
            "status": "TODO",
            "note": "LSPosed requires building and installing an Xposed module APK.",
        }

    return {"status": "ERROR", "error": f"Unknown method: {method}. Use: frida, hluda, frida-attach, gadget, lsposed"}


